#include <stdio.h>
#include <stdlib.h>
#include "dog.h"
/**
 *init_dog - intializes struct dog
 *@d: the structure to initialize
 *@name: name of the dog
 *@age: age of the dog
 *@owner: owner of the dog
 *
 *Return: nothing
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if (d != NULL)
	{
		d->name;
		d->age;
		d->owner;
	}
}
